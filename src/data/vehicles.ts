export const getVehiclesByClass = (
  vehicleClass: VehicleClass,
  driveType?: 'ev' | 'ice'
): Vehicle[] => {
  const evFiltered = electricVehicles.filter(v => v.vehicleClass === vehicleClass);
  const iceFiltered = iceVehicles.filter(v => v.vehicleClass === vehicleClass);

  if (driveType === 'ev') return evFiltered;
  if (driveType === 'ice') return iceFiltered;
  return [...evFiltered, ...iceFiltered];
};

export const getVehicleById = (id: string): Vehicle | undefined => {
  return [...electricVehicles, ...iceVehicles].find(v => v.id === id);
};

export const getDefaultEVForClass = (vehicleClass: VehicleClass): Vehicle | undefined => {
  return electricVehicles.find(v => v.vehicleClass === vehicleClass);
};

export const getDefaultICEForClass = (vehicleClass: VehicleClass): Vehicle | undefined => {
  return iceVehicles.find(v => v.vehicleClass === vehicleClass);
};

